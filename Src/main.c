/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */
  

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_ADC1_Init();
  MX_ADC2_Init();
  MX_DAC1_Init();

  /* USER CODE BEGIN 2 */

  uint8_t tempByte = 0;
  uint16_t pulseAmplitude = 0;
  uint32_t pulseDuration = 0;
  uint8_t loopCounter = 0;
  //application specific output initialization
  ANACTRL_SETLD3(0);
  HAL_DAC_SetValue(&hdac1, DAC_CHANNEL_1, DAC_ALIGN_12B_R, 0x0);
  HAL_DAC_Start(&hdac1, DAC_CHANNEL_1);


  uint32_t onTick;
  uint32_t offTick;
  uint32_t currentTick;
  uint32_t pwmPeriod=0x1f; //32msec
  uint32_t pwmDutyCycle=50;
  HAL_IncTick();

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
	  /* USER CODE BEGIN WHILE */

  	  //read switch bits
	  tempByte = anaCtrl_readByte();

#ifdef ENABLE_DAC_CONTROL
	  //pulse Duration is lower nibble & 10
	  pulseDuration = (tempByte & 0xf) * 10;
	  //pulse Amplitude is upper nibble & 256
	  pulseAmplitude = (tempByte >> 4) << 8;

  	  //light LED bimodal based on loop counter
	  loopCounter&1?ANACTRL_SETLD3(1):ANACTRL_SETLD3(0);

	  //set pulse amplitude
	  HAL_DAC_SetValue(&hdac1, DAC_CHANNEL_1, DAC_ALIGN_12B_R, pulseAmplitude);
	  //pause for pulse duration
	  if(pulseDuration)
		  HAL_Delay(pulseDuration);
	  HAL_DAC_SetValue(&hdac1, DAC_CHANNEL_1, DAC_ALIGN_12B_R, 0x0);

	  //Balance of 1 second as pulse off time
	  HAL_Delay(1000-pulseDuration);
	  loopCounter++;
#endif //ENABLE_DAC_CONTROL


#ifdef ENABLE_GPIO_PWM_CONTROL
	  //reduce resolution from 256 steps (8-bits) to 32 (msec) steps (5-bits)
	  pwmDutyCycle = (tempByte>>3);
	  //adjust for a +1msec error due to use of HAL function calls
	  pwmDutyCycle = (tempByte>>3) - 1;

	  currentTick = HAL_GetTick();
	  if(currentTick>onTick)
	  {
		  //start of current period - turn GPIO on
		  ANACTRL_SETGPIOOUT0(1);
		  //start of next period
		  onTick=currentTick + pwmPeriod;
		  //when to turn GPIO off
		  offTick=currentTick + pwmDutyCycle;
	  }
	  if(currentTick>offTick)
		  //end of pwmDutyCycle turn GPIO off
		  ANACTRL_SETGPIOOUT0(0);

#endif //ENABLE_GPIO_PWM_CONTROL

	  /* USER CODE END WHILE */
  }
  /* USER CODE END 3 */
}

